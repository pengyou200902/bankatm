    private void submit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_buttonActionPerformed
        // TODO add your handling code here:
        
        
        if (withdraw_text.getText().toString().isEmpty()){
        javax.swing.JOptionPane.showMessageDialog(this,"Withdraw amount cannot be empty","Withdraw message",javax.swing.JOptionPane.WARNING_MESSAGE);  
        }
        else{
            double amt = Double.parseDouble(withdraw_text.getText().toString());
            
            if (amt > this.balance){
                javax.swing.JOptionPane.showMessageDialog(this,"Withdraw amount cannot be greater than balance","Withdraw message",javax.swing.JOptionPane.WARNING_MESSAGE);  
            }
            else{
                
                BaseCurrency crr = new BaseCurrency(this.type,amt);
                OpResponse res = this.bank_account_controller.withdraw(this.user_bank_account,crr);
                if(res.status){
                    javax.swing.JOptionPane.showMessageDialog(this,res.response,"Withdraw message",javax.swing.JOptionPane.PLAIN_MESSAGE);  

                }
                else{
                    javax.swing.JOptionPane.showMessageDialog(this,res.response,"Withdraw message",javax.swing.JOptionPane.WARNING_MESSAGE);  
                }

            }
        }
    }//GEN-LAST:event_submit_buttonActionPerformed

