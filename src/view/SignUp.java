    private void submit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_buttonActionPerformed
        // TODO add your handling code here:
         String name = "";
         String address = "";
         String birthday = "";
         String username = "";
         String password = "";
         StringBuilder errors = new StringBuilder();
         
         if (name_text.getText().isBlank()){
            errors.append("Name cannot be empty\n");
        }   
        else{
            name = name_text.getText();
        }

        
        if (address_Text.getText().isBlank()){
            errors.append("Address cannot be empty\n");
        }  
        else{
            address = address_Text.getText();
        }
        
        if (birthday_text.getText().isBlank()){
            errors.append("Birth date cannot be empty\n");
        }  
        else{
            birthday = birthday_text.getText();
        }
        
        
        if (username_text.getText().isBlank()){
            errors.append("Username cannot be empty\n");
        }  
        else{
            username = username_text.getText();
        }
        
                
        if (new String(password_text.getPassword()).isBlank()){
            errors.append("Password cannot be empty\n");
        }  
        else{
            password = new String(password_text.getPassword());
        } 
        
        if (errors.length()>0){
            javax.swing.JOptionPane.showMessageDialog(this,errors,"Error",javax.swing.JOptionPane.WARNING_MESSAGE);
        }
        else{
            if(this.login_controller.signUp(name,address,birthday,username,password)){
               javax.swing.JOptionPane.showMessageDialog(this,"Sign Up successfull","Message",javax.swing.JOptionPane.PLAIN_MESSAGE);
            }
            else{
               javax.swing.JOptionPane.showMessageDialog(this,"Sign Up unsuccessfull","Message",javax.swing.JOptionPane.WARNING_MESSAGE);
            }
        }
        
    }

